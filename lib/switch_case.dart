void main() {
  // Switch case Statements :Applicable for only 'int' and String
  String grade = 'C';
  switch (grade) {
    case 'A':
      print("Excellent grade of A");
      break;
    case 'B':
      print("very Good ");
      break;
    case 'c':
      print("good enough. But work Hard");
      break;
    default:
      print("You've Failed");
  }
}
